package sessionS9S1

fun main(){
    val entero: Int = 42
    val largo: Long = 1000000000L
    val decimal: Double = 3.1416
    val flotante: Float = 3.14f
    val caracter: Char = 'A'
    val booleano: Boolean = true
    val texto: String = "Hola Kotlin"

    // Tipo Any (supertipo de todos)
    val cualquiera: Any = 42

    println("Entero: $entero")
    println("Long: $largo")
    println("Decimal: $decimal")
    println("Flotante: $flotante")
    println("Char: $caracter")
    println("Boolean: $booleano")
    println("String: $texto")
    println("Any: $cualquiera")
}